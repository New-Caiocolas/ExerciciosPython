nome = input('digite algo:')
print('O tipo primitivo de {} é da {}!!'.format(nome,type(nome)))
print('só tem espaços? {}'.format(nome.isspace()))
print('É um numero? {}'.format(nome.isnumeric()))
print('É alfabético? {}'.format(nome.isalpha()))
print('É alfanumérico? {}'.format(nome.isalnum()))
print('Está em letras maiusculas? {}'.format(nome.isupper()))
print('Está em letras minusculas? {}'.format(nome.islower()))
print('Está capitalisada? {}'.format(nome.istitle()))
